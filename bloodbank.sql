CREATE TABLE if not exists DONOR (
DONOR_ID	NUMERIC(15)	PRIMARY KEY,
NAME		VARCHAR(10)	NOT NULL,
CONTACT		NUMERIC(10)	NOT NULL,
AGE		NUMERIC(10)	NOT NULL,	
SEX		VARCHAR(10)	NOT NULL,
BLOOD_GRP	VARCHAR(10)	NOT NULL,
WEIGHT		NUMERIC(10)	NOT NULL);

CREATE TABLE if not exists ACCEPTOR (
ACCEPTOR_ID	NUMERIC(15)	PRIMARY KEY,
NAME		VARCHAR(10)	NOT NULL,
CONTACT		NUMERIC(10)	NOT NULL,
AGE		NUMERIC(10)	NOT NULL,	
SEX		VARCHAR(10)	NOT NULL,
BLOOD_GRP	VARCHAR(10) 	NOT NULL,
HOSPITAL_NAME	VARCHAR(15)	NOT NULL,
CAUSE		VARCHAR(15)	NOT NULL);

CREATE TABLE if not exists BLOOD_INVENTORY (
BRANCH_ID	NUMERIC(10)	PRIMARY KEY,
BRANCH_NAME	VARCHAR(20)	NOT NULL,
ADDRESS		VARCHAR(50)	NOT NULL,
EMAIL_ID	VARCHAR(50)	NOT NULL);

CREATE TABLE if not exists STOCK (
STOCK_ID		VARCHAR(20),	
BRANCH_ID		NUMERIC(10),
BLOOD_GRP		VARCHAR(10)	NOT NULL,
QUANTITY		NUMERIC(10)	NOT NULL,
CONSTRAINT	PK_STOCK	PRIMARY KEY(STOCK_ID, BRANCH_ID), 
CONSTRAINT	FK_STOCK_BLOOD_INVENTORY	FOREIGN KEY(BRANCH_ID) REFERENCES BLOOD_INVENTORY(BRANCH_ID));

CREATE TABLE if not exists CAMP (
CAMP_ID		VARCHAR(20)	PRIMARY KEY,
LOCATION	VARCHAR(20)	NOT NULL,
DATE		DATE		NOT NULL,
TIME		TIME		NOT NULL,
BRANCH_ID	NUMERIC(10)	NOT NULL,
CONSTRAINT	FK_CAMP_BLOOD_INVENTORY		FOREIGN KEY(BRANCH_ID) REFERENCES BLOOD_INVENTORY(BRANCH_ID));

CREATE TABLE if not exists EMPLOYEE (
EMP_ID		NUMERIC(15)	PRIMARY KEY,
NAME		VARCHAR(10)	NOT NULL,
CONTACT		NUMERIC(10)	NOT NULL,		
SEX		VARCHAR(10)	NOT NULL,
SALARY		NUMERIC(10)	NOT NULL,
DATE_JOINED	DATE		NOT NULL,
BRANCH_ID	NUMERIC(10)	NOT NULL,
DESIGNATION	VARCHAR(20)	NOT NULL,
QUALIFICATION	VARCHAR(20)	NOT NULL,
CONSTRAINT	FK_EMPLOYEE_BLOOD_INVENTORY	FOREIGN KEY(BRANCH_ID) REFERENCES BLOOD_INVENTORY(BRANCH_ID));

CREATE TABLE if not exists PARTICIPATED_IN (
CAMP_ID		VARCHAR(20),
DONOR_ID	NUMERIC(10),
CONSTRAINT 	PK_PARTICIPATED_IN		PRIMARY KEY(CAMP_ID, DONOR_ID)
CONSTRAINT	FK_PARTICIPATED_IN_CAMP		FOREIGN KEY(CAMP_ID) REFERENCES CAMP(CAMP_ID)	
CONSTRAINT	FK_PARTICIPATED_IN_DONOR	FOREIGN KEY(DONOR_ID) REFERENCES DONOR(DONOR_ID));

CREATE TABLE if not exists DONATES (
DONOR_ID	NUMERIC(10),
BRANCH_ID	NUMERIC(10),
NO_OF_BAGS	NUMERIC(10)	NOT NULL,
CONSTRAINT	PK_DONATES			PRIMARY KEY(DONOR_ID, BRANCH_ID),
CONSTRAINT	FK_DONATES_DONOR		FOREIGN KEY(DONOR_ID) REFERENCES DONOR(DONOR_ID),
CONSTRAINT	FK_DONATES_BLOOD_INVENTORY	FOREIGN KEY(BRANCH_ID) REFERENCES BLOOD_INVENTORY(BRANCH_ID));

CREATE TABLE if not exists RECEIVES (
ACCEPTOR_ID	NUMERIC(10),
BRANCH_ID	NUMERIC(10),
RECEIVING_DATE	DATE		NOT NULL,
CONSTRAINT	PK_RECEIVES			PRIMARY KEY(ACCEPTOR_ID, BRANCH_ID,RECEIVING_DATE),
CONSTRAINT	FK_RECEIVES_ACCEPTOR		FOREIGN KEY(ACCEPTOR_ID) REFERENCES ACCEPTOR(ACCEPTOR_ID),
CONSTRAINT	FK_RECEIVES_BLOOD_INVENTORY	FOREIGN KEY(BRANCH_ID) REFERENCES BLOOD_INVENTORY(BRANCH_ID));

CREATE TABLE if not exists WORKS_AT (
EMP_ID		NUMERIC(10),
CAMP_ID		VARCHAR(20),
CONSTRAINT	PK_WORKS_AT			PRIMARY KEY(EMP_ID, CAMP_ID),
CONSTRAINT	FK_WORKS_AT_EMPLOYEE		FOREIGN KEY(EMP_ID) REFERENCES EMPLOYEE(EMP_ID),
CONSTRAINT	FK_WORKS_AT_CAMP		FOREIGN KEY(CAMP_ID) REFERENCES CAMP(CAMP_ID));


